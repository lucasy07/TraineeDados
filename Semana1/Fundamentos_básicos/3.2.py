a = 10
print(f"Valor inicial: {a}")
a += 5
print(f"Após somar 5: {a}")
a -= 3
print(f"Após subtrair 3: {a}")
a *= 2
print(f"Após multiplicar por 2: {a}")
a /= 4
print(f"Após dividir por 4: {a}")
a //= 2
print(f"Após divisão inteira por 2: {a}")
a %= 3
print(f"Após resto da divisão por 3: {a}")
a **= 2
print(f"Após elevar à potência 2: {a}")
